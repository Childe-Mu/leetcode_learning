public class No37_solveSudoku {

    public static void main(String[] args) {
        char[][] board = {
                {'5', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '9'}
        };
        new No37_solveSudoku().solveSudoku_v1(board);
        System.out.println(Arrays.deepToString(board));
    }

    private int[] col;
    private int[] row;
    private int[] area;

    public void solveSudoku_v1(char[][] board) {
        this.col = new int[9];
        this.row = new int[9];
        this.area = new int[9];
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                char c = board[i][j];
                if (c == '.') {
                    continue;
                }
                int x = c - '0';
                row[i] |= 1 << x;
                col[j] |= 1 << x;
                int k = j / 3 + i / 3 * 3;
                area[k] |= 1 << x;
            }
        }
        backtrack_v1(board, 0, 0);
    }

    private boolean backtrack_v1(char[][] b, int i, int j) {
        if (j == 9) {
            i++;
            j = 0;
        }
        while (i < 9 && j < 9 && b[i][j] != '.') {
            i = i + (j + 1) / 9;
            j = (j + 1) % 9;
        }
        if (i >= 9 || j >= 9) {
            return true;
        }
        int k = j / 3 + i / 3 * 3;
        for (int x = 1; x <= 9; x++) {
            if (((row[i] >> x) & 1) == 1 || ((col[j] >> x) & 1) == 1 || ((area[k] >> x) & 1) == 1) {
                continue;
            }
            b[i][j] = (char) ('0' + x);
            row[i] |= 1 << x;
            col[j] |= 1 << x;
            area[k] |= 1 << x;
            if (backtrack_v1(b, i, j + 1)) {
                return true;
            }
            b[i][j] = '.';
            row[i] ^= 1 << x;
            col[j] ^= 1 << x;
            area[k] ^= 1 << x;
        }
        return false;
    }
}
